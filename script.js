async function loadSongsFromGoogleSheet() {
    const fetchedData = {};
    for (const sheetName of SHEET_NAMES) {
        try {
            // AÏó¥Î∂ÄÌÑ∞ EÏó¥ÍπåÏßÄ (artist, title, youtubeUrl, albumCoverUrl, difficulty ÏàúÏÑúÎùºÍ≥† Í∞ÄÏ†ï)
            const response = await gapi.client.sheets.spreadsheets.values.get({
                spreadsheetId: SPREADSHEET_ID,
                range: `${sheetName}!A:E`, // A:EÎ°ú Î≤îÏúÑ ÌôïÏû• (difficulty Ìè¨Ìï®)
            });

            const values = response.result.values;
            if (values && values.length > 1) {
                const headers = values[0].map(h => h.toLowerCase());
                const songs = values.slice(1).map(row => {
                    let song = {};
                    headers.forEach((header, index) => {
                        song[header] = row[index];
                    });
                    if (!song.youtubeurl) song.youtubeurl = '';
                    if (!song.albumcoverurl) song.albumcoverurl = '';
                    if (!song.difficulty) song.difficulty = ''; // difficulty Ï∂îÍ∞Ä
                    // Í∞Å ÎÖ∏ÎûòÏóê Ïπ¥ÌÖåÍ≥†Î¶¨ Ï†ïÎ≥¥ Ï∂îÍ∞Ä
                    let category = '';
                    if (sheetName === 'a') category = 'K-POP';
                    else if (sheetName === 'b') category = 'POP';
                    else if (sheetName === 'c') category = 'J-POP';
                    song.category = category;
                    return song;
                });
                fetchedData[sheetName] = songs;
            } else {
                fetchedData[sheetName] = [];
                console.warn(`ÏãúÌä∏ '${sheetName}'Ïóê Îç∞Ïù¥ÌÑ∞Í∞Ä ÏóÜÏäµÎãàÎã§.`);
            }
        } catch (error) {
            console.error(`ÏãúÌä∏ '${sheetName}'ÏóêÏÑú Îç∞Ïù¥ÌÑ∞Î•º Î∂àÎü¨Ïò§Îäî Îç∞ Ïã§Ìå®ÌñàÏäµÎãàÎã§:`, error);
            fetchedData[sheetName] = [];
        }
    }
    return fetchedData;
}

